package com.cekeh.utility;

/**
 * Cekeh's Vector4i object
 * Created 06/04/2018
 * @author Thomas vanBommel (TvB)
 */
public class Vector4i {

	public int x;
	public int y;
	public int z;
	public int w;
	
	/**
	 * Create a new Vector4i object
	 * @param x The x value
	 * @param y The y value
	 * @param z The z value
	 * @param w The w value
	 */
	public Vector4i(int x, int y, int z, int w) {
		this.x = x;
		this.y = y;
		this.z = z;
		this.w = w;
	}
}
