package com.cekeh.utility;

/**
 * Cekeh's Vector2f object
 * Created 06/04/2018
 * @author Thomas vanBommel (TvB)
 */
public class Vector2f {

	public float x;
	public float y;
	
	/**
	 * Create a new Vector2f object
	 * @param x The x value
	 * @param y The y value
	 */
	public Vector2f(float x, float y) {
		this.x = x;
		this.y = y;
	}
}
