package com.cekeh.utility;

/**
 * Cekeh's Vector2i object
 * Created 06/04/2018
 * @author Thomas vanBommel (TvB)
 */
public class Vector2i {

	public int x;
	public int y;
	
	/**
	 * Create a new Vector2i object
	 * @param x The x value
	 * @param y The y value
	 */
	public Vector2i(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
