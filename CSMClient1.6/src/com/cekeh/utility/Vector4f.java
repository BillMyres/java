package com.cekeh.utility;

/**
 * Cekeh's Vector4f object
 * Created 06/04/2018
 * @author Thomas vanBommel (TvB)
 */
public class Vector4f {

	public float x;
	public float y;
	public float z;
	public float w;
	
	/**
	 * Create a new Vector4f object
	 * @param x The x value
	 * @param y The y value
	 * @param z The z value
	 * @param w The w value
	 */
	public Vector4f(float x, float y, float z, float w) {
		this.x = x;
		this.y = y;
		this.z = z;
		this.w = w;
	}
}
