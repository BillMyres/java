package com.cekeh.utility;

/**
 * Cekeh's Vector3i object
 * Created 06/04/2018
 * @author Thomas vanBommel (TvB)
 */
public class Vector3i {

	public int x;
	public int y;
	public int z;
	
	/**
	 * Create a new Vector3i object
	 * @param x The x value
	 * @param y The y value
	 * @param z The z value
	 */
	public Vector3i(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
}
